import { GrammarTopic, PracticeQuestion, TheorySection, TopicPriority } from '../types/grammar';

type TopicSeed = {
  id: string;
  title: string;
  priority: TopicPriority;
  difficulty: GrammarTopic['difficulty'];
  shortDescription: string;
  tags: string[];
  objectives: string[];
  sections: Array<{ title: string; explanation: string; bullets: string[] }>;
  rules: Array<{ rule: string; note?: string }>;
  examplePairs: Array<{ sentence: string; explanation: string }>;
  mistakes: Array<{ incorrect: string; correct: string; explanation: string }>;
  revision: string[];
  practice: Array<{
    type: PracticeQuestion['type'];
    prompt: string;
    options?: string[];
    answer: string;
    explanation: string;
  }>;
  relatedTopics: string[];
};

const toTheorySections = (topicId: string, sections: TopicSeed['sections']): TheorySection[] =>
  sections.map((section, index) => ({
    id: `${topicId}-section-${index + 1}`,
    title: section.title,
    explanation: section.explanation,
    bullets: section.bullets,
  }));

const createTopic = (seed: TopicSeed): GrammarTopic => ({
  id: seed.id,
  title: seed.title,
  level: 'B2',
  priority: seed.priority,
  shortDescription: seed.shortDescription,
  difficulty: seed.difficulty,
  tags: seed.tags,
  learningObjectives: seed.objectives,
  theorySections: toTheorySections(seed.id, seed.sections),
  grammarRules: seed.rules.map((rule, index) => ({
    id: `${seed.id}-rule-${index + 1}`,
    ...rule,
  })),
  examples: seed.examplePairs.map((example, index) => ({
    id: `${seed.id}-example-${index + 1}`,
    ...example,
  })),
  commonMistakes: seed.mistakes.map((mistake, index) => ({
    id: `${seed.id}-mistake-${index + 1}`,
    ...mistake,
  })),
  quickRevision: seed.revision,
  practiceQuestions: seed.practice.map((question, index) => ({
    id: `${seed.id}-practice-${index + 1}`,
    ...question,
  })),
  relatedTopics: seed.relatedTopics,
});

